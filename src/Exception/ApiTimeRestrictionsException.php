/**
 * Исключение технического перерыва WB API.
 *
 * Бросается в двух случаях:
 *   1. HTTP 429 с сообщением, содержащим "Технический перерыв до HH:MM" —
 *      WB объявляет плановое окно обслуживания, в течение которого API недоступен.
 *   2. HTTP 400 с полем `errorText`, содержащим "временные ограничения" —
 *      запрос отклонён из-за временного ограничения работы конкретного метода.
 *
 * В отличие от обычного 429 (ApiClientException), этот случай не должен
 * обрабатываться через retry — нужно ждать окончания перерыва.
 * Время окончания перерыва можно извлечь из `getMessage()`.
 */
